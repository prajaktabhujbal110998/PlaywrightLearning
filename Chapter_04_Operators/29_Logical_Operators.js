//&& ---> AND Gate
// || ---> OR Gate

let a = true;
let b = false;
console.log(a && b); 
console.log(a || b);
console.log(!a); //NOT operator - It will give the opposite value of the variable - reversed of a
console.log(!b); //NOT operator - It will give the opposite value of the variable - reversed of b

//AND Gate - Both the conditions should be true to get the result true
//OR Gate - At least one condition should be true to get the result true