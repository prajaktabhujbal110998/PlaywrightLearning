//Identifiers, Literals, Operators
var a = 10;
a = 20;
console.log(a);

//Variable name = Identifier - a (name which is given to the container)
//Variable value = Literal - 10 , 20 , "Hello" , true , false, null, undefined
//Operator = (assignment operator) , + , - , * , / , % (arithmetic operators) 

//Var - Keyword to declare a variable
var a1 = 10    //In Java Script semicolon is optional
console.log(a1);

//Rules for naming identifiers in JavaScript:
//1.Must start with a letter, underscore, or dollar sign.
//2.Can contain letters, numbers, underscores, and dollar signs.
//3.Cannot start with a number.
//4.Cannot be a reserved keyword (e.g., var, function, if, else, break).
//5.Cannot contain spaces.
//6.Cannot contain special characters other than underscore and dollar sign.
//7.Case sensitive (e.g., myVariable and myvariable are different).
//8.After the first character, identifiers can include letters, digits, underscores, or dollar signs.


var name = "Hello";
console.log(name);

var $name = "Hello";
console.log($name);

var _name = "Hello";
console.log(_name);

var name1 = "Hello";
console.log(name1);

var name_1 = "Hello";
console.log(name_1);

var name$1 = "Hello";
console.log(name$1);

var name_1$1 = "Hello";
console.log(name_1$1);

var name_1$1_2 = "Hello";
console.log(name_1$1_2);

//Case sensitive
var Name = "Prajakta";
console.log(Name);

var name = "Prajakta";
console.log(name);


